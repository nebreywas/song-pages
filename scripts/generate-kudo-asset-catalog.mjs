#!/usr/bin/env node
/**
 * Build kudoAssetCatalog.generated.ts from src/assets/images/kudo-images.
 * Run: node scripts/generate-kudo-asset-catalog.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, '..');
const assetDir = path.join(root, 'src/assets/images/kudo-images');
const outFile = path.join(root, 'src/kudos/catalog/kudoAssetCatalog.generated.ts');

const VARIANT_SUFFIX = {
  'single-color': 'single-color-image',
  grays: 'grays-image',
};

function titleCase(id) {
  return id
    .replace(/([a-z])([A-Z])/g, '$1 $2')
    .replace(/[-_]+/g, ' ')
    .replace(/\b\w/g, (c) => c.toUpperCase());
}

function importId(elementId, variantKey) {
  return `asset_${elementId.replace(/[^a-z0-9]+/gi, '_')}_${variantKey.replace(/-/g, '_')}`;
}

const files = fs.readdirSync(assetDir).filter((name) => name.endsWith('.png'));
const byElement = new Map();

for (const filename of files) {
  let elementId = null;
  let variant = null;
  for (const [variantKey, suffix] of Object.entries(VARIANT_SUFFIX)) {
    const token = `-${suffix}.png`;
    if (filename.endsWith(token)) {
      elementId = filename.slice(0, -token.length);
      variant = variantKey;
      break;
    }
  }
  if (!elementId || !variant) continue;

  if (!byElement.has(elementId)) byElement.set(elementId, {});
  byElement.get(elementId)[variant] = filename;
}

const sortedIds = [...byElement.keys()].sort();
const importLines = [];
const catalogRows = [];

for (const elementId of sortedIds) {
  const variants = byElement.get(elementId);
  const variantEntries = [];
  for (const variantKey of ['single-color', 'grays']) {
    const filename = variants[variantKey];
    if (!filename) continue;
    const id = importId(elementId, variantKey);
    importLines.push(`import ${id} from '../../assets/images/kudo-images/${filename}';`);
    variantEntries.push(`    '${variantKey}': ${id},`);
  }
  catalogRows.push(`  {
    id: '${elementId}',
    label: '${titleCase(elementId)}',
    variants: {
${variantEntries.join('\n')}
    },
  },`);
}

const source = `/** Generated by scripts/generate-kudo-asset-catalog.mjs — do not edit. */
${importLines.join('\n')}

export type KudoAssetVariant = 'single-color' | 'grays';

export type BuiltInKudoAsset = {
  id: string;
  label: string;
  variants: Partial<Record<KudoAssetVariant, string>>;
};

export const KUDO_ASSET_CATALOG: BuiltInKudoAsset[] = [
${catalogRows.join('\n')}
];

export const KUDO_ASSET_IDS = KUDO_ASSET_CATALOG.map((row) => row.id);

export function findKudoAsset(assetId: string): BuiltInKudoAsset | undefined {
  return KUDO_ASSET_CATALOG.find((row) => row.id === assetId);
}
`;

fs.mkdirSync(path.dirname(outFile), { recursive: true });
fs.writeFileSync(outFile, source);
console.log(`Wrote ${sortedIds.length} Kudo assets → ${path.relative(root, outFile)}`);
