/**
 * Lightweight heuristic POS — English-biased, marked as heuristic (not compromise).
 * Good enough for Demo 2-lite without a heavy NLP dependency.
 */
export function classifyLexicalRole(normalized: string): LexicalRole {
  if (!normalized) return 'unknown';
  if (PRONOUNS.has(normalized)) return 'pronoun';
  if (DETERMINERS.has(normalized)) return 'determiner';
  if (CONNECTORS.has(normalized)) return 'connector';
  if (COMMON_ADVERBS.has(normalized)) return 'adverb';
  if (COMMON_VERBS.has(normalized)) return 'verb';
  if (COMMON_ADJECTIVES.has(normalized)) return 'adjective';
  if (normalized.length > 2 && !STOP_WORDS.has(normalized)) {
    if (normalized.endsWith('ly') && normalized.length > 4) return 'adverb';
    if (normalized.endsWith('ing') || normalized.endsWith('ed')) return 'verb';
    return 'noun';
  }
  if (STOP_WORDS.has(normalized)) return 'connector';
  return 'unknown';
}

export function isStopWord(normalized: string): boolean {
  return STOP_WORDS.has(normalized);
}

export function isPivotWord(normalized: string): boolean {
  return PIVOT_WORDS.has(normalized);
}

export function isContentWord(normalized: string, role: LexicalRole): boolean {
  if (isStopWord(normalized) && role !== 'verb' && role !== 'noun' && role !== 'adjective') {
    return false;
  }
  return POS_CONTENT_WEIGHT[role] >= 0.45;
}
